-- Calculate contrasting text color
local function get_contrast_color(hex)
  local r = tonumber(hex:sub(2, 3), 16)
  local g = tonumber(hex:sub(4, 5), 16)
  local b = tonumber(hex:sub(6, 7), 16)
  local luminance = (0.299 * r + 0.587 * g + 0.114 * b) / 255
  return luminance > 0.5 and "#000000" or "#ffffff"
end

-- Namespace for our highlights
local ns_id = vim.api.nvim_create_namespace("weresocool_colors")

-- Create highlight groups for all colors
function M.setup_highlights()
  for name, hex in pairs(M.colors) do
    local fg = get_contrast_color(hex)
    vim.api.nvim_set_hl(0, "WscColor_" .. name, { bg = hex, fg = fg })
  end
end

-- Update highlights for a buffer
function M.update_buffer(bufnr)
  bufnr = bufnr or vim.api.nvim_get_current_buf()

  -- Clear existing highlights
  vim.api.nvim_buf_clear_namespace(bufnr, ns_id, 0, -1)

  -- Get all lines
  local lines = vim.api.nvim_buf_get_lines(bufnr, 0, -1, false)

  for lnum, line in ipairs(lines) do
    local line_lower = line:lower()
    -- Check each color name
    for name, _ in pairs(M.colors) do
      local start_idx = 1
      while true do
        local s, e = line_lower:find(name, start_idx, true)  -- plain text search
        if not s then break end

        -- Check word boundaries
        local before_ok = (s == 1) or not line_lower:sub(s-1, s-1):match("[%w_]")
        local after_ok = (e == #line_lower) or not line_lower:sub(e+1, e+1):match("[%w_]")

        if before_ok and after_ok then
          -- Apply highlight (0-indexed)
          vim.api.nvim_buf_add_highlight(bufnr, ns_id, "WscColor_" .. name, lnum - 1, s - 1, e)
        end

        start_idx = e + 1
      end
    end
  end
end

-- Setup autocommands
function M.setup()
  M.setup_highlights()

  local group = vim.api.nvim_create_augroup("WereSoCoolColors", { clear = true })

  -- Initial update for current buffer
  vim.api.nvim_create_autocmd("FileType", {
    group = group,
    pattern = "socool",
    callback = function(ev)
      M.update_buffer(ev.buf)
    end,
  })

  -- Update on text change (debounced)
  vim.api.nvim_create_autocmd({ "TextChanged", "TextChangedI" }, {
    group = group,
    pattern = "*.socool",
    callback = function(ev)
      vim.defer_fn(function()
        if vim.api.nvim_buf_is_valid(ev.buf) then
          M.update_buffer(ev.buf)
        end
      end, 100)
    end,
  })

  -- Update when entering buffer
  vim.api.nvim_create_autocmd("BufEnter", {
    group = group,
    pattern = "*.socool",
    callback = function(ev)
      M.update_buffer(ev.buf)
    end,
  })
end

return M
